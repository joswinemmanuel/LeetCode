class Solution(object):
    def merge(self, nums1, m, nums2, n):
        """
        :type nums1: List[int]
        :type m: int
        :type nums2: List[int]
        :type n: int
        :rtype: None Do not return anything, modify nums1 in-place instead.
        """
        
        result = nums1[:m] + nums2[:n]
        # [result.append(nums1[i]) for i in range(m)]
        # [result.append(nums2[i]) for i in range(n)]
        
        result.sort()
        
        for i in range(m+n):
            nums1[i] = result[i]

